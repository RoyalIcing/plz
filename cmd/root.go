package cmd

import (
	"fmt"
	"github.com/spf13/cobra"
	"os"

	homedir "github.com/mitchellh/go-homedir"
	"github.com/spf13/viper"
)

var cfgFile string

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "plz [command]",
	Short: "Search for useful developer things such as links, documentation & specs",
	Long:  `Search for links, documentation, specs: IETFâ€™s RFCs, HTTP statuses, MDN, WAI-ARIA`,
	Args:  cobra.ExactArgs(1),
	Run: func(cmd *cobra.Command, args []string) {
		term := args[0]

		results := Find(term)
		if len(results) == 0 {
			fmt.Println(fmt.Errorf("No results found for '%s'", term))
			os.Exit(1)
		}

		for _, result := range results {
			fmt.Println(result)
		}
	},
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

